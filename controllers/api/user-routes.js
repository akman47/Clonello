const router = require('express').Router();
const { User, Project } = require('../../models');

// GET all users
router.get('/', (req, res) => {
    User.findAll({
        attributes: { exclude: ['password'] },
        attributes: ['id', 'username'],
        include: [
            {
                model: Project,
                attributes: ['title']
            }
        ]
    })
    .then(dbUserData => res.json(dbUserData))
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

// GET one user
